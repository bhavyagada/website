  <h2>What to blog about?</h2>
  <small>
    <i>Note: Just dumping my thoughts here. This might be incoherent or off-topic, but it's published for personal reasons!</i>
  </small>
  <p>
    I seriously have no clue. I read other bloggers, and it feels like they all have it figured out. Their websites look amazing, and their content is interesting and useful.
  </p>
  <p>
    I always wonder when I'll be like that. Why don't I have unique thoughts? Why would my writing be useful to anyone?
  </p>
  <p>
    When I find a writer I like, I skim through everything they've published. Some have been doing this for decades, others are newer but still have tons to say.
  </p>
  <p>
    But when I foolishly compare myself to them, I forget about the persistence it takes. They didn't create their blogs overnight—they built them one word at a time. It takes effort!
  </p>
  <p>
    After some more reflection, I realized it's not about comparing myself to others. I'm afraid of putting in the effort—repeatedly, over time—until something takes shape. It's even hard for me to write this. My mind wants to shut off the laptop and go back to doomscrolling. I have to fight through it.
  </p>
  <p>
    There's a saying that if you say you want to do something but don't, you never really wanted to do it. Also, that we often wish for the finished product but don't enjoy the process of creating it.
  </p>
  <p>
    This seems true for me, but I tend to believe it's not. I could easily use AI to write for me instead of going through this struggle. It's the hard work that's hard. And I'll push through.
  </p>
  <p>
    I used to procrastinate because I felt my writing needed to be novel and insightful to be accepted. But who cares? I'm doing this for myself, not to follow some arbitrary rules. This self-imposed pressure is a mental trap that does nothing but hold me back. Most rules in life are made up, so I can break them or create new ones.
  </p>
  <p>
    I can write whatever I want and publish it. Lately, I've felt like everything I write is “shit” in my eyes—not because it's bad, but because I've held it in for so long.
  </p>
  <p>
    Letting go of the bad will make room for the good. It'll help me make mistakes, learn, and see myself improving along the way.
  </p>
  <p>
    I can't doubt myself until I've written at least 100 blogs. There's a reason #100daysof_anything_ works. The path won't be easy, but I can at least make it fun!
  </p>
</main>
